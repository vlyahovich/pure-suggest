function getSearchVariants($str) {
    global $ENG_TABLE, $KBR_MISS_TABLE, $REV_KBR_MISS_TABLE;

    $str = mb_strtolower($str);

    return array_unique([
        $str,
        strtr($str, $ENG_TABLE),
        strtr($str, $KBR_MISS_TABLE),
        strtr($str, $REV_KBR_MISS_TABLE)
    ]);
